<?php

$price = 200;

print("price = $price \n");
print("");

// + Suma
print("price + 100 = " . ($price + 100) . "\n");

// - Resta
print("price - 10 = " . ($price - 10) . "\n");

// * Multiplicacion
print("price * 2 = " . ($price * 2) . "\n");

// / Division
print("price / 3 = " . ($price / 3) . "\n");

// % Modulo
print("price % 3 = " . ($price % 3) . "\n");

// ** Potencia
print("price ** 2 = " . ($price ** 2) . "\n");
